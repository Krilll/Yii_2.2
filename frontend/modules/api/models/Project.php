     * @return \yii\db\ActiveQuery
     */
    public function getProjectTasks()
    {
        return static::hasMany(Task::className(), ['project_id' => 'id']);
    }


    public function fields()
    {
        return [
            'id',
            'title',
            'description_short' =>  [['description'], 'max' => 50],
        ];
    }

    public function extraFields()
